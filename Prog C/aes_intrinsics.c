int subword(int word)
{
    int a=(sbox[word<<0>>24]<<24)|(sbox[word<<8>>24]<<16)|(sbox[word<<16>>24]<<8)|(sbox[word<<24>>24]<<0);
    return a;
}

int rotword(int word)
{
    int a=word>>8|word<<24;
    return a;
}

