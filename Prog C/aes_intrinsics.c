void int_array_to_char(int *a, char *b, int n)
{
    for(int i=0;i<n;i++)
    {
        b[4*i+0]=a[i]>>24;
        b[4*i+1]=a[i]>>16;
        b[4*i+2]=a[i]>>8;
        b[4*i+3]=a[i];
    }
    return;
}

int aes_ecb_encrypt(unsigned char *data, unsigned long length, char *key, int keysize) { // use expanded key
    if(!length%16 == 0) return 1;
    if(keysize != 128 && keysize != 192 && keysize != 256) return 1;
    __m128i tmp;

    length = length/16;
    int j;
    for(int i=0; i < length; i++) {
        tmp = _mm_loadu_si128 (&((__m128i*)data)[i]);
        tmp = _mm_xor_si128 (tmp,((__m128i*)key)[0]);
        for(j=1;j<keysize/64;j++) {
            tmp = _mm_aesenc_si128 (tmp,((__m128i*)key)[j]);
        }
        tmp = _mm_aesenclast_si128 (tmp,((__m128i*)key)[j]);
        _mm_storeu_si128(&((__m128i*)data)[i],tmp);
    }
    return 0;
}

